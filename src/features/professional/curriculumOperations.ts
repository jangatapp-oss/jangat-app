function positioning(subject: string, evidencePrecision: string): PositioningQuestion[] {
  return DIMENSIONS.map(({ dimension, action, proof }) => ({
    dimension,
    prompt: `${action} ${subject} ? Décrivez une situation précise, votre degré d’autonomie et le résultat obtenu.`,
    evidenceHint: `${proof} ${evidencePrecision}`,
  }));
}

function advice(subject: string): Record<TrackRecommendation, string> {
  return {
    direct_validation: `Vos acquis sur ${subject} sont solides : consultez la mise à jour réglementaire, puis soumettez directement la mission probatoire.`,
    short_update: `Actualisez les règles et outils récents de ${subject}, réalisez le contrôle guidé, puis passez à la mission.`,
    consolidation: `Reprenez la méthode complète de ${subject}, entraînez-vous sur le cas LOGIX FORMA et faites corriger votre première production.`,
    full_course: `Suivez toutes les séquences de ${subject}, réalisez d’abord la version sans IA, puis la version assistée et son audit.`,
    advanced_mission: `Pilotez la mission de niveau direction sur ${subject}, défendez vos arbitrages et transmettez la méthode à un collaborateur.`,
  };
}

